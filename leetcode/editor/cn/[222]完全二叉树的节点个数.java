/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    public int countNodes(TreeNode root) {
        if (root == null)
            return 0;
        int h = 1;
        TreeNode treeroot;
        treeroot = root;
        while (root.left != null) {
            h++;
            root = root.left;
        }
        int val = 0;
        int left = 1 << (h - 1), right = (1 << h) - 1;
        while (left != right) {
            val = (right - left + 1) / 2 + left;
            if (testval(treeroot, h, val))
                left = val;
            else right = val - 1;
        }
        return left;
    }

    public boolean testval(TreeNode root, int h, int val) {
        int bits = 1 << (h - 2);
        TreeNode node = root;
        while (node != null && bits > 0) {
            if ((bits & val) == 0) {
                node = node.left;
            } else {
                node = node.right;
            }
            bits >>= 1;
        }
        return node != null;
    }
}
